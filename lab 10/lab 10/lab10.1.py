import matplotlib.pyplot as plt
# Data points for the line
x = [1, 2, 3, 4, 5]
y = [2, 4, 6, 8, 10]
# Plotting the line
plt.plot(x, y)
# Adding labels and title
plt.xlabel("X Axis - Numbers")
plt.ylabel("Y Axis - Doubled Values")
plt.title("Simple Line Plot")
# Displaying the plot
plt.show()
